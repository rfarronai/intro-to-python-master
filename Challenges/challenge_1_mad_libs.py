# Write a story with some words missing


# Ask the user to provide the missing words
colour = input(f"What color are Roses: ")
colour2 = input(f"What color are Violets: ")
sugar = input(f"What do you think about sugar: ")

story = "Roses are " + colour + "\nViolets are " + colour2 + "\nSugar is " + sugar + "\nAnd so are you"

# Display the final story
print(f"{story}")
